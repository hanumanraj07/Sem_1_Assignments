var celcius;
celcius = 0;

if(celcius <= 0)
{
    console.log("Freezing Point");
}
else if(celcius > 0 && celcius <=15)
{
    console.log("Very Cold");
}
else if(celcius > 15 && celcius <=25)
{
    console.log("Cold");
}
else if(celcius > 25 && celcius <=35)
{
    console.log("Warm");
}
else if(celcius >=35)
{
    console.log("Hot");
}